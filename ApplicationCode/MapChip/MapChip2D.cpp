void MapChip2D::Ins()
{
	//ステージ作成
	MapChipData();

	for (int i = 0; i < sizeY_.size(); i++)
	{
		for (int j = 0; j < sizeX_.size(); j++)
		{
			//チップそれぞれの番号によって作成
			if (mapChipData[i][j] == (int)MapInfo::NONE)
			{
				chipData[i][j] = ChipData::Create(i, j, ImageName::mapChipTest);

			}
			
			if (mapChipData[i][j] == (int)MapInfo::ONES)
			{
				chipData[i][j] = ChipData::Create(i, j, ImageName::mapChipTest2);
			}
		}
	}
}

void MapChip2D::Update(Vector2 MousePos)
{
	XMFLOAT2 mousePos = MousePos;
	for (int i = 0; i < sizeY_.size(); i++)
	{
		for (int j = 0; j < sizeX_.size(); j++)
		{
			bool isHit = Collision::HitBox(chipData[i][j]->GetPos(), chipData[i][j]->GetHalfSize(), mousePos);
			if (isHit)
			{
				chipData[i][j]->SetDrawFlag(isHit);
			}
			else
			{
				chipData[i][j]->SetDrawFlag(isHit);

			}
			
		}
	}
}

void MapChip2D::Draw()
{
	for (int i = 0; i < sizeY_.size(); i++)
	{
		for (int j = 0; j < sizeX_.size(); j++)
		{
			if (chipData[i][j]->GetDrawFlag() == true)
			{
				chipData[i][j]->Draw();

			}
		}
	}
}

void MapChip2D::Delete()
{
	for (int i = 0; i < sizeY_.size(); i++)
	{
		for (int j = 0; j < sizeX_.size(); j++)
		{

			chipData[i][j]->Delete();
		}
	}
}
