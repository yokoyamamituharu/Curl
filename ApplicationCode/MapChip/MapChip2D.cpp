void MapChip2D::ChipDataCreate()
{
	for (int i = 0; i < sizeY_.size(); i++)
	{
		for (int j = 0; j < sizeX_.size(); j++)
		{
			//チップそれぞれの番号によって作成
			if (mapChipData[i][j] == (int)MapInfo::NONE)
			{
				chipData[i][j] = ChipData::Create(i, j, ImageName::mapChipTest);
				chipData[i][j]->CostOn();

			}

			if (mapChipData[i][j] == (int)MapInfo::ONE)
			{
				chipData[i][j] = ChipData::Create(i, j, ImageName::mapChipTest2);
			}

			if (mapChipData[i][j] == (int)MapInfo::WALL)
			{
				chipData[i][j] = ChipData::Create(i, j, ImageName::mapChipTest3);
				
			}

			if (mapChipData[i][j] == (int)MapInfo::GORL)
			{
				chipData[i][j] = ChipData::Create(i, j, ImageName::mapChipTest);
				chipData[i][j]->CostOn();
			}
		}
	}
}

void MapChip2D::CostDataCreate()
{

	for (int i = 0; i < sizeY_.size(); i++)
	{
		for (int j = 0; j < sizeX_.size(); j++)
		{
			if (chipData[i][j]->GetCost() == 1)
			{
				mapChipCostData[i][j] = 1;
			}
			else
			{
				mapChipCostData[i][j] = 0;
			}

		}
	}
}

void MapChip2D::Ins()
{
	//ステージ作成
	MapChipDataCreate();

	//チップデータの作成
	ChipDataCreate();

	//コストデータ作成
	CostDataCreate();

}

void MapChip2D::Update(Vector2 MousePos)
{
	XMFLOAT2 mousePos = MousePos;
	for (int i = 0; i < sizeY_.size(); i++)
	{
		for (int j = 0; j < sizeX_.size(); j++)
		{
			bool isHit = Collision::HitBox(chipData[i][j]->GetPos(),(int)chipData[i][j]->GetHalfSize(), MousePos);
			if (isHit)
			{
				chipData[i][j]->SetDrawFlag(isHit);
			}
			else
			{
				chipData[i][j]->SetDrawFlag(isHit);
			}
			
		}
	}

}

void MapChip2D::Draw()
{
	
	for (int i = 0; i < sizeY_.size(); i++)
	{
		for (int j = 0; j < sizeX_.size(); j++)
		{
			if (chipData[i][j]->GetCost() == 0)
			{
				chipData[i][j]->Draw();

			}
			//chipData[i][j]->Draw();
			if (chipData[i][j]->GetDrawFlag() == true&& chipData[i][j]->GetCost() == 1)
			{
				chipData[i][j]->Draw();

			}
		}
	}
}

void MapChip2D::Delete()
{
	for (int i = 0; i < sizeY_.size(); i++)
	{
		for (int j = 0; j < sizeX_.size(); j++)
		{

			chipData[i][j]->Delete();
		}
	}
}
